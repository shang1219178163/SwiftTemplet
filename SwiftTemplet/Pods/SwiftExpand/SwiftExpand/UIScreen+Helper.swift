import UIKit


public extension UIScreen {
    
    public static var width : CGFloat {
        get {
            return UIScreen.main.bounds.size.width
        }
    }
    
    public static var height : CGFloat {
        get {
            return UIScreen.main.bounds.size.height
        }
    }
    
    
  
    
}
